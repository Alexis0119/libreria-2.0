    private void bingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bingresarActionPerformed
        String usuario = tusuario.getText();
        String password = tpass.getText();

        if(usuario.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(null, "Algun campo vacio");
        }else{
            if(usuario.equals("Cliente123") && password.equals("1234")){
                JOptionPane.showMessageDialog(null, "Bienvenido cliente");
                clientes fadm = new clientes ();
                fadm.setVisible(true);
                this.dispose();
            }else{
                if(usuario.equals("Admin123") && password.equals("1234")){
                JOptionPane.showMessageDialog(null, "Bienvenido Administrador");
                Frmprincipal fadm = new Frmprincipal ();
                fadm.setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Su usuario o contrase√±a son incorrectos");
                }
            }
        }
        
    }//GEN-LAST:event_bingresarActionPerformed

