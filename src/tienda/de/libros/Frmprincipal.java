    void cargar (String valor){
        String [] titulos={"id","titulo","autor","editorial","año","precio"};
        String [] registros = new String[6];
        
        String sql = "SELECT * FROM admin where Titulo_libro LIKE '%"+valor+"%'";
        
        model = new DefaultTableModel(null,titulos);
        
        conectar cc= new conectar();
        Connection cn = cc.conexion();
        
        try {
                    Statement st = cn.createStatement();
                    ResultSet rs = st.executeQuery(sql);
                    
                    while(rs.next()){
                        registros[0]= rs.getString("id_producto");
                        registros[1]= rs.getString("Titulo_libro");
                        registros[2]= rs.getString("Autor_libro");
                        registros[3]= rs.getString("Editorial_libro");
                        registros[4]= rs.getString("Año_libro");
                        registros[5]= rs.getString("Precio_libro");
                        model.addRow(registros);
                    }
                    tdatos.setModel(model);                   
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }       
    }
    
