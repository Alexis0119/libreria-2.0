    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         
             conectar cc = new conectar();
             Connection cn = cc.conexion();
             String titulo,autor,editorial,año,precio;
             String sql;
             titulo = ttitulo.getText();
             autor = tautor.getText();
             editorial = teditorial.getText();
             año = taño.getText();
             precio = tprecio.getText();
             sql="INSERT INTO admin (Titulo_libro, Autor_libro, Editorial_libro, Año_libro, Precio_libro) VALUES (?,?,?,?,?)";
             try {
             PreparedStatement pst = cn.prepareStatement(sql);
             pst.setString(1,titulo);
             pst.setString(2,autor);
             pst.setString(3,editorial);
             pst.setString(4,año);
             pst.setString(5,precio);
             int n= pst.executeUpdate();
             if (n>0){
                 JOptionPane.showMessageDialog(null, "Registro guardado con exito");
                 bloquear();
                 cargar("");
             }
         } catch (SQLException ex) {
             Logger.getLogger(Frmprincipal.class.getName()).log(Level.SEVERE, null, ex);
         }
            
        
    }//GEN-LAST:event_jButton2ActionPerformed

